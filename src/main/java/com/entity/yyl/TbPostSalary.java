package com.entity.yyl;


public class TbPostSalary {

  private long postId;
  private long basicSalary;
  private long jobsSalary;


  public long getPostId() {
    return postId;
  }

  public void setPostId(long postId) {
    this.postId = postId;
  }


  public long getBasicSalary() {
    return basicSalary;
  }

  public void setBasicSalary(long basicSalary) {
    this.basicSalary = basicSalary;
  }


  public long getJobsSalary() {
    return jobsSalary;
  }

  public void setJobsSalary(long jobsSalary) {
    this.jobsSalary = jobsSalary;
  }

}
